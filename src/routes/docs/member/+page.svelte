<h2>Member</h2>

<h3>GET</h3>

<p>
	Selecteer één <em>member</em> in whois.fdnd.nl op basis van een meegestuurde url parameter met het
	id.
</p>

<h4>Url parameters:</h4>
<ul>
	<li>
		<strong>id</strong>, het id van de te selecteren <em>member</em>.
		<br />(Vind jouw id door de lijst met members te gebruiken!)
	</li>
</ul>

<h4>Voorbeeld Request:</h4>

<ul>
	<li>
		<a href="/api/v1/member?id=cldczhjad16yh0av08jxscp0a"
			>/api/v1/member?id=cldczhjad16yh0av08jxscp0a</a
		>
	</li>
</ul>

